void outputArrBit(int (&bit)[U], char name)//Вывод массива битов
{
    std::cout
            << "\n=========================================================================================================\n";
    std::cout << "В виде масива битов множество " << name << ": ";
    for (int i = 0; i < U - 1; ++i)
        if (bit[i])
            std::cout << bit[i];
        else std::cout << "_";
    std::cout << "\nМножесто " << name << ": ";
    for (int i = 0; i < U; ++i) {
        if (bit[i] == 1) {
            std::cout << Universum[i];
        }
    }
    std::cout
            << "\n=========================================================================================================\n";
}
