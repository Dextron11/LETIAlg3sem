void outputComWorld(char name, int num) //Вывод  "машинное  слово"
{
    std::cout
            << "\n=========================================================================================================\n";
    std::cout << "В виде машинного слова множесто " << name << ":";
    for (int i = U - 2; i >= 0; --i)
        std::cout << ((num >> i) & 1);
    std::cout << "\nМножество " << name << ": ";
    for (int i = 0; i <= U; i++)
        if ((num >> i) & 1)
            std::cout << Universum[i];
    std::cout
            << "\n=========================================================================================================\n";

}
